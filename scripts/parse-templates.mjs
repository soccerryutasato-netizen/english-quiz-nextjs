import { readFileSync, writeFileSync } from "fs";

const raw = readFileSync(
  new URL("../data/all_templates.txt", import.meta.url),
  "utf8"
);

// Split on ⭐️テンプレ
const templateBlocks = raw.split(/(?=⭐️テンプレ)/u).filter((b) => b.trim() && b.includes("✅"));

const circledMap = {
  "①":1,"②":2,"③":3,"④":4,"⑤":5,"⑥":6,"⑦":7,"⑧":8,"⑨":9,"⑩":10,
  "⑪":11,"⑫":12,"⑬":13,"⑭":14,"⑮":15,"⑯":16,"⑰":17,"⑱":18,"⑲":19,"⑳":20
};

function parseTemplateNum(s) {
  if (circledMap[s]) return circledMap[s];
  // full-width digits or ASCII
  const n = parseInt(s.replace(/[１-９]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0xFEE0)));
  return isNaN(n) ? null : n;
}

const templates = [];

for (const block of templateBlocks) {
  // First line: ⭐️テンプレXXX...
  const firstLine = block.split("\n")[0];

  // Extract template number (handles ①②…, ８９, 10 11, etc.)
  const numMatch = firstLine.match(/テンプレ([①-⑳\d０-９]+)/u);
  if (!numMatch) continue;
  const templateNum = parseTemplateNum(numMatch[1]);
  if (!templateNum) continue;

  // Extract pattern from 「...」 in first 5 lines (may span multiple lines)
  const headerLines = block.split("\n").slice(0, 6).join(" ");
  const patternMatch = headerLines.match(/[「『](.+?)[」』]/);
  if (!patternMatch) continue;
  const pattern = patternMatch[1]
    .replace(/〜|〇〇/g, "___")
    .trim();

  // Split into question blocks: lines starting with digit+) or ） (handles full-width digits too)
  const questionBlocks = block.split(/\n(?=[\d１-９０]+[)））])/u).slice(1);

  const questions = [];

  for (const qb of questionBlocks) {
    // Japanese prompt: first line after the question number
    const firstQLine = qb.split("\n")[0];
    const promptRaw = firstQLine.replace(/^[\d１-９０]+[)））]\s*/, "").trim();
    const promptJa = promptRaw.replace(/^[「『]|[」』]$/g, "").trim();
    if (!promptJa) continue;

    // Sample answer
    const answerMatch = qb.match(/✅答え[：:]\s*(.+)/);
    if (!answerMatch) continue;
    const sampleAnswer = answerMatch[1].trim();

    // Pronunciation (first 🔊 line)
    const pronMatch = qb.match(/🔊発音[：:]\s*(.+)/);
    const pronunciation = pronMatch ? pronMatch[1].trim() : "";

    // Explanation (💡解説)
    const expMatch = qb.match(/💡解説[：:]\s*(.+)/);
    const explanation = expMatch ? expMatch[1].trim() : "";

    // Hint: last 2-3 words of sample answer
    const words = sampleAnswer.split(/\s+/);
    const hint = words.slice(-Math.min(3, words.length)).join(" ");

    questions.push({ promptJa, sampleAnswer, pronunciation, explanation, hint });
  }

  if (questions.length < 5) continue;

  templates.push({ num: templateNum, pattern, questions });
}

// Sort by template number
templates.sort((a, b) => a.num - b.num);

// Generate choices for Level 4: use other answers in the same template
for (const tmpl of templates) {
  const allAnswers = tmpl.questions.map((q) => q.sampleAnswer);
  for (let i = 0; i < tmpl.questions.length; i++) {
    const correct = tmpl.questions[i].sampleAnswer;
    const wrongPool = allAnswers.filter((a) => a !== correct);

    // Pick 3 different wrong answers
    const wrongs = [];
    const indices = new Set();
    for (let attempt = 0; wrongs.length < 3 && attempt < wrongPool.length; attempt++) {
      const idx = (i * 7 + attempt * 13) % wrongPool.length;
      if (!indices.has(idx)) {
        indices.add(idx);
        wrongs.push(wrongPool[idx]);
      }
    }
    // Fill if still not enough
    for (let j = 0; wrongs.length < 3 && j < wrongPool.length; j++) {
      if (!wrongs.includes(wrongPool[j])) wrongs.push(wrongPool[j]);
    }

    const choices = [correct, ...wrongs.slice(0, 3)];
    // Deterministic shuffle
    for (let j = choices.length - 1; j > 0; j--) {
      const k = (i * 31 + j * 17) % (j + 1);
      [choices[j], choices[k]] = [choices[k], choices[j]];
    }
    tmpl.questions[i].choices = choices;
  }
}

// Generate TypeScript
const ts = `// Auto-generated by scripts/parse-templates.mjs — DO NOT EDIT MANUALLY

export interface RawQuestion {
  promptJa: string;
  sampleAnswer: string;
  pronunciation: string;
  explanation: string;
  hint: string;
  choices: string[];
}

export interface RawTemplate {
  num: number;
  pattern: string;
  questions: RawQuestion[];
}

export const rawTemplates: RawTemplate[] = ${JSON.stringify(templates, null, 2)};
`;

writeFileSync(new URL("../lib/rawTemplates.ts", import.meta.url), ts);
console.log(`Generated ${templates.length} templates, total questions: ${templates.reduce((s, t) => s + t.questions.length, 0)}`);
templates.forEach((t) => console.log(`  テンプレ${t.num}: ${t.pattern} (${t.questions.length}問)`));
