const SYSTEM_PROMPT = `あなたは英語教育の専門家で、日本人向けのわかりやすい英語解説を書くのが得意です。

以下の構成でテンプレ解説を書いてください（マークダウン形式）：

1. **導入** - 英語は「単語の積み重ね」で考えると分かりやすいというメッセージ（❌⭕ で対比）
2. **テンプレ全体の意味** - なぜそういう意味になるのか？（H1見出し）
3. **各キーワードの解説** - テンプレの主要な単語を1つずつ（H1見出しで各単語）
   - 意味の説明
   - 日常的な使い方の例
   - イメージ（絵が浮かぶような説明）
4. **全部つなげると？** - 直訳から自然な日本語へ（H1見出し）
5. **よく似た表現との違い**（H1見出し）- なぜそのテンプレを使うのか
6. **丸ごと覚えるなら** - 例文4つ（✅マーク付き）
7. **まとめ** - 🔹箇条書きでキーワードの意味、最後に ##で太字のまとめ

スタイル：
- 絵文字を適度に使う（😊✨🔥👉等）
- 短い段落で読みやすく
- 専門用語は必ず簡単な言葉で説明する
- 日本人が間違いやすいポイントを強調する
- **太字** で重要ワードを強調

参考例（テンプレ①の解説）：
${EXAMPLE_EXPLANATION}

上記と同じ温かみのある文体・構成で書いてください。`;

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

async function generateExplanation(template) {
  const { num, pattern, questions } = template;
  const exampleAnswers = questions.slice(0, 5).map(q => `「${q.promptJa}」→ ${q.sampleAnswer}`).join("\n");

  const userPrompt = `以下の英語テンプレの解説を書いてください。

テンプレ番号: ${num}
テンプレパターン: ${pattern}

例題（参考）:
${exampleAnswers}

このテンプレを使った自然な日本語文と英文の対応、各単語の意味を丁寧に解説してください。`;

  const message = await client.messages.create({
    model: "claude-opus-4-8",
    max_tokens: 2000,
    system: SYSTEM_PROMPT,
    messages: [{ role: "user", content: userPrompt }],
  });

  return message.content[0].type === "text" ? message.content[0].text : "";
}

async function main() {
  console.log(`🚀 解説生成開始: ${rawTemplates.length}テンプレ`);

  const results = { ...cache };

  for (const template of rawTemplates) {
    const key = String(template.num);
    if (results[key]) {
      console.log(`  ⏭  テンプレ${template.num}: キャッシュ済みスキップ`);
      continue;
    }

    console.log(`  ✏️  テンプレ${template.num}: ${template.pattern} を生成中...`);
    try {
      const explanation = await generateExplanation(template);
      results[key] = explanation;

      // キャッシュ保存（途中で止まっても再開できる）
      writeFileSync(CACHE_FILE, JSON.stringify(results, null, 2));
      console.log(`  ✅ テンプレ${template.num}: 完了`);

      // API rate limit 対策
      await new Promise(r => setTimeout(r, 500));
    } catch (err) {
      console.error(`  ❌ テンプレ${template.num}: エラー`, err.message);
    }
  }

  // templateExplanations.ts を生成
  const entries = Object.entries(results)
    .sort(([a], [b]) => Number(a) - Number(b))
    .map(([num, text]) => {
      const escaped = text.replace(/`/g, "\\`").replace(/\${/g, "\\${");
      return `  ${num}: \`${escaped}\``;
    })
    .join(",\n\n");

  const output = `// テンプレごとの解説テキスト（マークダウン形式）
// Auto-generated by scripts/generate-explanations.mjs
// キー = テンプレ番号

export const templateExplanations: Record<number, string> = {
${entries},
};
`;

  writeFileSync(join(ROOT, "lib/templateExplanations.ts"), output);
  console.log(`\n🎉 完了! ${Object.keys(results).length}テンプレの解説を lib/templateExplanations.ts に書き出しました`);
}

main().catch(console.error);
